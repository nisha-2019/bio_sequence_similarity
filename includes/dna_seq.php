      <td width="804" height="37" align="justify"><p><b>DNA sequencing</b></p>
      <p>DNA sequencing is the process of determining the precise order of nucleotides within a DNA molecule. It includes any method or technology that is used to determine the order of the four basesguanine, cytosine, and thymine—in a strand of DNA. The advent of rapid DNA sequencing methods has greatly accelerated biological and medical research and discovery.</p>
      <p>Knowledge of DNA sequences has become indispensable for basic biological research, and in numerous applied fields such as diagnostic, biotechnology,forensic biology, virology and biological systematics. The rapid speed of sequencing attained with modern DNA sequencing technology has been instrumental in the sequencing of complete DNA sequences, or genomes of numerous types and species of life, including the human genome and other complete DNA sequences of many animal, plant, and microbial species.</p>
    An example of the results of automated chain-termination DNA sequencing.</div>
      
      <p>The first DNA sequences were obtained in the early 1970s by academic researchers using laborious methods based on two-dimensional chromatography. </p>
      <p>The canonical structure of DNA has four bases: Thymine (T), Adenine (A), Cytosine (C), and Guanine (G). DNA sequencing is the determination of the physical order of these bases in a molecule of DNA. However, there are many other bases that may be present in a molecule. In some viruses (specifically, bacteriophage, cytosine may be replaced by hydroxy methyl or hydroxy methyl glucose cytosine. In mammalian DNA, variant bases with methyl</a> groups or phosphosulfate may be found. Depending on the sequencing technique, a particular modification may or may not be detected, e.g., the 5mC (methyl cytosine) common in humans may or may not be detected.</p>
