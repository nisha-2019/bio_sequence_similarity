      <td align="justify"><p>Ribonucleic acid (RNA) is a linear molecule composed of four types of smaller molecules called ribonucleotide bases: adenine (A), cytosine (C), guanine (G), and uracil (U). RNA is often compared to a copy from a reference book, or a template, because it carries the same information as its DNA template but is not used for long-term storage.</p>
        <p>Each ribonucleotide base consists of a ribose sugar, a phosphate group, and a nitrogenous base. Adjacent ribose nucleotide bases are chemically attached to one another in a chain via chemical bonds called phosphodiester bonds. Unlike DNA, RNA is usually single-stranded. Additionally, RNA contains ribose sugars rather than deoxyribose sugars, which makes RNA more unstable and more prone to degradation.</p>
        <p>RNA is synthesized from DNA by an enzyme known as RNA polymerase during a process called transcription. The new RNA sequences are complementary to their DNA template, rather than being identical copies of the template. RNA is then translated into proteins by structures called ribosomes. There are three types of RNA involved in the translation process: messenger RNA (mRNA), transfer RNA (tRNA), and ribosomal RNA (rRNA).</p>
      <p>Although some RNA molecules are passive copies of DNA, many play crucial, active roles in the cell. For example, some RNA molecules are involved in switching genes on and off, and other RNA molecules make up the critical protein synthesis machinery in ribosomes.</p>
