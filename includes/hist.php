<p>Origin of bioinformatic/biological databses: </p>
<p><br />
  The first bioinformatic/biological databases were constructed a few years after the first protein sequences began to become available. The first protein sequence reported was that of bovine insulin in 1956 , consisting of 51 residues. Nearly a decade later, the first nucleic acid sequence was reported, that of yeast alanine tRNA with 77 bases. Just a year later, Dayhoff gathered all the availablesequence data to create the first bioinformatic database . <br />
  The Protein DataBank followed in 1972 with a collectionof ten X-ray crystallographic protein structures, and theSWISSPROT protein sequence database began in 1987. A huge variety of divergent data resources of different typesand sizes are now available either in the public domain or more recently from commercial third parties. All of the original databases were organised in a very simpleway with data entries being stored in flat files, either one perentry, or as a single large text file. Re-write - Later on lookup indexes were added to allow convenient keyword searching of header information.</p>
