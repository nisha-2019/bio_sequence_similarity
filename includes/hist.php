      <td height="554" align="justify"><p>By 1981, 579 human genes had been mapped and mapping by in situ hybridization had become a standard method. Marvin Carruthers and Leory Hood made a huge leap in bioinformatics when they invented a method for automated DNA sequencing.  In 1988, the Human Genome organization (HUGO) was founded. This is an international organization of scientists involved in Human Genome Project. In 1989, the first complete genome map was published of the bacteria Haemophilus influenza .</p>
        <p>By 1991, a total of 1879 human genes had been mapped. In 1993, Genethon, a human genome research center in France Produced a physical map of the human genome. Three years later, Genethon published the final version of the Human Genetic Map. This concluded the end of the first phase of the Human Genome Project.        </p>
        <p>Bioinformatics was fuelled by the need to create huge databases, such as GenBank and EMBL and DNA Database of Japan to store and compare the DNA sequence data erupting from the human genome and other genome sequencing projects. <br />
        Today, bioinformatics embraces protein structure analysis, gene and protein functional information, data from patients, pre-clinical and clinical trials, and the metabolic pathways of numerous species.</p>
