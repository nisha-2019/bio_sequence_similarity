      <td align="justify"><p><b>Amino Acids</b></p>
       
        <p>Twenty percent of the human body is made up of protein. Protein plays a crucial role in almost all biological processes and amino acids are the building blocks of it.</p>
        <p>A large proportion of our cells, muscles and tissue is made up of amino acids, meaning they carry out many important bodily functions, such as giving cells their structure. They also play a key role in the transport and the storage of nutrients. Amino acids have an influence on the function of organs, glands, tendons and arteries. They are furthermore essential for healing wounds and repairing tissue, especially in the muscles, bones, skin and hair as well as for the removal of all kinds of waste deposits produced in connection with the metabolism.</p>
        <b>What types of amino acids?</b><br />
        <p>Amino acids are organic compounds which contain at least one amino group (-NH2) and a carboxy (-COOH) group. In the human genome, 20 amino acids are created to build proteins and therefore termed proteinogen. Besides this, there are approximately 250 amino acids which do not form proteins. These are used to form sugar for example.</p>
        <p>The 20 proteinogen amino acids are also called standard amino acids, which can be divided into three groups: essential, semi-essential and non-essential. </p>
        <p>Eight amino acids are essential for humans, as the body cannot produce them by themselves, and they have to be supplied externally. These are: isoleucine, leucine, lysine, methionine, phenylalanine, threonine, tryptophan and valine.  </p>
        <p>Arginine and histidine form the group of so-called semi-essential amino acids. They have to be consumed in the diet under certain circumstances.</p>
      <p>The ten non-essential amino acids are able to be produced in the body. The following amino acids fall into this category: alanine, asparagine, aspartic acid, cysteine,glutamine, glutamic acid, glycine, proline, serin and tyrosine.</p>
      <p>From a biochemical point of view, the proteinogen amino acids can be differentiated through their structure. They come in two symmetrical forms: the L- and D- structure. The L- amino acid structure is the only one natural to the body. This is why it is important to be aware when choosing additionally required amino acids, e.g. through dietary supplements, that the ingredients contain L- and not a D- or DL- (mixed) amino acid form.</p></td>
