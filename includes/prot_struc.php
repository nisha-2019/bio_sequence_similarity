      <td align="justify"><p>Protein structure is the biomolecular structure of a protein molecule. Proteins are polymers â€“ specifically polypeptides -sequences .Each unit of a protein is called an amino acid residue because it is the residue of every amino acid that forms the protein by losing a water molecule. By convention, a chain under 40 residues is often identified as a peptide, rather than a protein. To be able to perform their biological function.</p>
      <p>There are four distinct levels of protein structure.</p>
      <p>1. Primary structure</p>
      <p>The primary structure of a protein refers to the linear sequence of amino acids in the polypeptide chain. The primary structure is held together by covalent bonds such as peptide bonds, which are made during the process of protein biosynthesis or translation.The primary structure of a protein is determined by the gene corresponding to the protein. A specific sequence of nucleotides in DNA is transcribed into mRNA, which is read by the ribosome in a process called translation.</p>
      <p><img src="./proimg/Peptide.gif" height="260" width="260"/></p>
      <p>2.Secondary structure</p>
      <p>Secondary structure refers to highly regular local sub-structures. Two main types of secondary structure, the alpha helix and the beta strand or beta sheets, were suggested in 1951 by Linus Pauling and coworkers.hese secondary structures are defined by patterns of hydrogen bonds between the main-chain peptide groups. They have a regular geometry.</p>
      <p><img src="./proimg/images.jpeg" height="260" width="260" /></p>
      <p>3.Tertiary structure      </p>
      <p>Tertiary structure refers to the three-dimensional structure of a single, double, or triple bonded protein molecule. The alpha-helixes and beta pleated-sheets are folded into a compact globular structure. The folding is driven by the non-specific hydrophobic interactions, the burial of hydrophobic residues from water, but the structure is stable only when the parts of a protein domain are locked into place by specific tertiary interactions, such as salt bridges, hydrogen bonds, and the tight packing of side chains and disulfide bonds. </p>
      <p><img src="./proimg/ter.jpeg" height="260" width="260" /></p>
      <p>4. Quaternary structure</p>
      <p>Quaternary structure is the three-dimensional structure of a multi-subunit protein and how the subunits fit together. In this context, the quaternary structure is stabilized by the same non-covalent interactions and disulfide bonds as the tertiary structure. Complexes of two or more polypeptides (i.e. multiple subunits) are called multimers. </p>
